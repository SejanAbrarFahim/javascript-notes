// In HTML, JavaScript code is inserted between <script> and </script> tags.

 // 01. JavaScript in <head>

 /*
  <!DOCTYPE html>
  <html lang="en">
  <head>
       <title>Document</title>
       <script>
          let date = "24/4/26"
          alert(date)
         </script>
    </head>
    <body>
    
    </body>
    </html>
 */



 // 02. JavaScript in <body> 

   /*
     <!DOCTYPE html>
     <html lang="en">
     <head>
       <title>Document</title>
     </head>
     <body>
    
        <script>
          let text = "Hello"
          alert(text)
        </script>
      </body>
      </html>
      */



  //  03. To use an external script, put the name of the script file in the src (source) attribute of a <script> tag: 

    /*
    <!DOCTYPE html>
    <html lang="en">
    <head>
        <title>Document</title>
    </head>
    <body>
    
     <script src="script.js">   
     </script>
    </body>
    </html>
    */